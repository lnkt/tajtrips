package com.tajway.tajwaycabs.responsedata;

public class TripCityData {
    private String id;
    private String booktrip_id;
    private String trip_city;
    private String trip_order;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBooktrip_id() {
        return booktrip_id;
    }

    public void setBooktrip_id(String booktrip_id) {
        this.booktrip_id = booktrip_id;
    }

    public String getTrip_city() {
        return trip_city;
    }

    public void setTrip_city(String trip_city) {
        this.trip_city = trip_city;
    }

    public String getTrip_order() {
        return trip_order;
    }

    public void setTrip_order(String trip_order) {
        this.trip_order = trip_order;
    }



}
